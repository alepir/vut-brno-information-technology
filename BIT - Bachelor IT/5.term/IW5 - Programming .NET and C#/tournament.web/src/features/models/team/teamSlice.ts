import { createAsyncThunk, createSlice, PayloadAction } from '@reduxjs/toolkit';
import { RootState, AppThunk } from '../../../app/store';
import teamClient from '../../../api/teamClient';
import Model from '../../../models/api/Model';
import Constants from "../../../utils/Constants";
export interface TeamState {
  records: Model[];
  selectedRecords: Model[];
  formDefaultModel: Model,
  status: 'idle' | 'loading' | 'failed' | 'notFetched';
  filter:{
    [key:string]:any,
    global?: string,
    name?: string,
    description?: string,
    country?: string,
  }
}

const initialState: TeamState = {
  records: [],
  selectedRecords: [],
  formDefaultModel: { id: Constants.GUID_EMPTY},
  status: 'notFetched',
  filter: {
    global: "",
    name: "",
    description: "",
    country: "",
  },
};

export const fetchAsync = createAsyncThunk(
  'team/fetch',
  async () => {
    try{
      const response = await teamClient.getAll({});
      // The value we return becomes the `fulfilled` action payload
      return response.data;
    }
    catch(exception){
      return null;
    }
  }
);

export const getByIdAsync = createAsyncThunk(
  'team/getById',
  async (id: string) => {
    try{
      const response = await teamClient.getById(id);
      // The value we return becomes the `fulfilled` action payload
      return response.data;
    }
    catch(exception){
      return null;
    }
  }
);

export const createAsync = createAsyncThunk(
  'team/create',
  async (payload: any) => {
    try{
      payload.image = payload.imagePath;
      const response = await teamClient.create(payload);
      // The value we return becomes the `fulfilled` action payload

      return response.data;
    }
    catch(exception){
      return null;
    }
  }
);

export const getFormDefaultModelAsync = createAsyncThunk(
  'team/getFormDefaultModel',
  async () => {
    try{
      const response = await teamClient.getFormDefaultModel();
      // The value we return becomes the `fulfilled` action payload
      return response.data;
    }
    catch{
      return null;
    }
  }
)


export const updateAsync = createAsyncThunk(
  'team/update',
  async (payload: any) => {
    try{
      const response = await teamClient.update(payload);
      // The value we return becomes the `fulfilled` action payload
      return response.data;
    }
    catch(exception){
      return null;
    }
  }
);

export const deleteAsync = createAsyncThunk(
  'team/delete',
  async (payload: any) => {
    try{
      await teamClient.delete(payload);
      // The value we return becomes the `fulfilled` action payload
      return payload;
    }
    catch(exception){
      return null;
    }
  }
);

export const teamSlice = createSlice({
  name: 'team',
  initialState,
  // The `reducers` field lets us define reducers and generate associated actions
  reducers: {
    addSelectedRecord: (state, action: PayloadAction<Model>) => {
      state.selectedRecords.push(action.payload);
    },
    delSelectedRecord: (state, action: PayloadAction<Model>) => {
      state.selectedRecords = state.selectedRecords.filter(x=> x.id !== action.payload.id);
    },
    clearSelectedRecords: (state) => {
      state.selectedRecords = [];
    },
    setFilter: (state, action: PayloadAction<{field: string, value: any}>) => {
      state.filter[action.payload.field] = action.payload.value;
    }
  },
  // The `extraReducers` field lets the slice handle actions defined elsewhere,
  // including actions generated by createAsyncThunk or in other slices.
  extraReducers: (builder) => {
    //fetch
    builder
      .addCase(fetchAsync.pending, (state) => {
        state.status = 'loading';
      })
      .addCase(fetchAsync.fulfilled, (state, action) => {
        if(action.payload !== null){
          state.status = 'idle';
          state.records = action.payload;
        }
        else{
          state.status = 'failed'
        }
      });

    //getById
    builder
      .addCase(getByIdAsync.pending, (state) => {
        state.status = 'loading';
      })
      .addCase(getByIdAsync.fulfilled, (state, action) => {
        if(action.payload !== null){
          state.status = 'idle';
          state.records = state.records.filter(x => x.id !== action.payload.id);
          
          state.records.push(action.payload);
          state.selectedRecords.push(action.payload);
        }
        else{
          state.status = 'failed'
        }
      });

    //formDefaultModel
    builder
    .addCase(getFormDefaultModelAsync.pending, (state) => {
      state.status = 'loading';
    })
    .addCase(getFormDefaultModelAsync.fulfilled, (state, action) => {
      if(action.payload !== null){
        state.status = 'idle';
        state.formDefaultModel = action.payload;
      }
      else{
        state.status = 'failed'
      }
    });

    //create
    builder
      .addCase(createAsync.pending, (state) => {
        state.status = 'loading';
      })
      .addCase(createAsync.fulfilled, (state, action) => {
        if(action.payload !== null){
          state.status = 'idle';
          state.records.push(action.payload);
        }
        else{
          state.status = 'failed';
        }
      });

      //update
      builder
      .addCase(updateAsync.pending, (state) => {
        state.status = 'loading';
      })
      .addCase(updateAsync.fulfilled, (state, action) => {
        if(action.payload !== null){
          state.status = 'idle';
          
          state.records = state.records.filter(x => x.id !== action.payload.id);
          state.selectedRecords = state.selectedRecords.filter(x => x.id !== action.payload.id);
          state.records.push(action.payload);
        }
        else{
          state.status = 'failed';
        }
      });

      //delete
      builder
      .addCase(deleteAsync.pending, (state) => {
        state.status = 'loading';
      })
      .addCase(deleteAsync.fulfilled, (state, action) => {
        if(action.payload !== null){
          state.status = 'idle';
          state.records = state.records.filter(x => x.id !== action.payload.id);
          state.selectedRecords = state.selectedRecords.filter(x => x.id !== action.payload.id);
        }
        else{
          state.status = 'failed';
        }
      });
      
  },
});


export const { addSelectedRecord, delSelectedRecord, clearSelectedRecords, setFilter } = teamSlice.actions;

// The function below is called a selector and allows us to select a value from
// the state. Selectors can also be defined inline where they're used instead of
// in the slice file. For example: `useSelector((state: RootState) => state.counter.value)`
export const selectAll = (state: RootState) => state.team.records;
export const selectById = (state: RootState, id: string) => state.team.records.filter(x=> x.id === id)[0] ?? { id: Constants.GUID_EMPTY};
export const selectIsSelected = (state: RootState, record:Model) => state.team.selectedRecords.filter(x => x.id === record.id).length !== 0;
export const selectFormDefaultModel = (state: RootState) => state.team.formDefaultModel;

export const selectFirstSelectedRecord = (state: RootState) => state.team.selectedRecords[0];
export const selectSelectedRecords = (state: RootState) => state.team.selectedRecords;

export const selectFilter = (state: RootState) => state.team.filter;

export const selectIsAnyRecordSelected = (state: RootState) => state.team.selectedRecords.length !== 0;
export const selectIsOneRecordSelected = (state: RootState) =>  state.team.selectedRecords.length === 1;

export const selectIsLoading = (state: RootState) => state.team.status === 'loading';
export const selectIsFetched = (state: RootState) => state.team.status !== 'notFetched';

export default teamSlice.reducer;
